#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	size_t	start;
	size_t	finish;

	if (s1 == NULL || set == NULL)
		return (NULL);
	start = 0;
	while (s1[start] && ft_strchr(set, s1[start]))
		start++;
	finish = ft_strlen(s1);
	while (finish > 0 && ft_strchr(set, s1[finish]))
		finish--;
	return (ft_substr(s1, start, ((finish + 1) - start)));
}	
