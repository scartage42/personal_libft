#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	sig;
	int	aux;

	i = 0;
	aux = 0;
	sig = 1;
	while (str[i] == ' ' || str[i] == '\f' || str[i] == '\n'
		|| str[i] == '\r' || str[i] == '\t' || str[i] == '\v')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sig *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		aux = (aux * 10) + (str[i] - 48);
		i++;
	}
	return (aux * sig);
}
