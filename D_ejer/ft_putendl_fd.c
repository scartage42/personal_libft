#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	if (fd < 0)
		return ;
	ft_putstr_fd(s, fd);  //pasamos a fd la informacion de la str
	write(fd, "\n", 1);	  //pasamos el salto del linea al fd
}
